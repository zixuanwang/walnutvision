<div class="title">关于易购时尚</div>
<p>易购时尚旨在帮助移动互联网用户迅捷买到中意的时尚商品。我们的运营方案主要解决购买时尚商品涉及的两大主要问题：买什么？如何买？用户只需将感兴趣的时尚商品信息收集到手机上，包括照片、时尚商品类别（手包，鞋子等）、时尚商品的特点（大小，材质等）、价格范围和地理位置，通过使用我们的产品，用户能够轻松锁定相同或者类似的时尚商品，以及这些商品在附近其他商店的销售价格和网店价格。我们的产品还可以凭借用户的搜索历史、朋友的反馈与喜好，建议相似或者匹配的时尚商品。本产品基于两部分核心算法：基于图像的时尚商品搜索和基于社交网络的推荐。基于图像的搜索技术以及基于位置的服务(LBS)，是我们独特的解决方案，在中国市场具有开创性。</p>
